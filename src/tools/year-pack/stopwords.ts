/**
 * Get stopwords for the specified language
 */
export function getStopwords(language: "en" | "zh"): Set<string> {
  return language === "zh" ? CHINESE_STOPWORDS : ENGLISH_STOPWORDS;
}

/**
 * Check if a term is a stopword
 */
export function isStopword(term: string, language: "en" | "zh"): boolean {
  const stopwords = getStopwords(language);
  return stopwords.has(term.toLowerCase());
}
